void USART1_IRQHandler(void){//串口1中断服务程序
	u8 Res;
	if(USART_GetITStatus(USART1, USART_IT_RXNE) != RESET){
		Res = USART_ReceiveData(USART1);//读取接收到的数据
		if((USART1_RX_STA&0x8000)==0){//接收未完成
			
			if(USART1_RX_STA&0x4000){//接收到了数据长度位
				if((USART1_RX_STA&0X3FFF) == USART1_RX_BUF[2]+4){
					//收到了足够长的数据，认为ok了
					USART1_RX_STA|=0x8000;
				}
			}
			else if((USART1_RX_STA&0X3FFF) == 3){
				USART1_RX_STA|=0x4000;//长度够了，认为收到了长度位
			}
			
			USART1_RX_BUF[USART1_RX_STA&0X3FFF]=Res;
			USART1_RX_STA++;
			if((USART1_RX_STA&0X3FFF)>(USART1_REC_LEN-1))	USART1_RX_STA=0;//接收数据错误,重新开始接收
		}
	}
}

void USART2_IRQHandler(void){//串口2中断服务程序
	//注意,读取USARTx->SR能避免莫名其妙的错误  
	u8 Res;
	if(USART_GetITStatus(USART2, USART_IT_RXNE) != RESET)  //接收中断(接收到的数据必须是0x0d 0x0a结尾)
		{
		Res = USART_ReceiveData(USART2);	//读取接收到的数据
		
		if((USART2_RX_STA&0x8000)==0)//接收未完成
			{
			if(USART2_RX_STA&0x4000)//接收到了0x0d
				{
				if(Res!=0x0a)USART2_RX_STA=0;//接收错误,重新开始
				else USART2_RX_STA|=0x8000;	//接收完成了 
				}
			else //还没收到0X0D
				{	
				if(Res==0x0d)USART2_RX_STA|=0x4000;
				else
					{
					USART2_RX_BUF[USART2_RX_STA&0X3FFF]=Res ;
					USART2_RX_STA++;
					if(USART2_RX_STA>(USART2_REC_LEN-1))USART2_RX_STA=0;//接收数据错误,重新开始接收	  
					}		 
				}
			}   		 
     }
} 

